a = int(input("Вкажіть сторону острова:"))
k = int(input("Вкажіть кількість Робінзонів:"))
n = int(input("Вкажіть норму площі острова:"))
s = a*a
m = s/k

if (m>=n):
    print("Місця достатньо")
    max_k = s // n
    extra = max_k - k
    print("Ще можна розмістити",extra,"Робінзонів")
else:
    print("місця не достатньо")